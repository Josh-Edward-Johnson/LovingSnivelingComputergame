using System;

class Program {
  public static void Main (string[] args) {
    //This program will divide, subtract, multiply and add to numbers and more!
    int a = 10;
    int b = 5;
    int c = 11;
    int d = 4;
    int e = 100;
    int f = 50;
    int g = 800;
    int h = 900;
